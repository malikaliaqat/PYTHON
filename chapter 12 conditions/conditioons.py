# Python support the logical condition from mathematics
a = 5
b = 6

# 1: eqaul
print (a==b)
# (5==6)

# 2: not equal
print(a!=b)
# (5!=6)

# 3: less than 
print(a<b)
# (5<6)

# 4: less than or eqal to
print(a<=b)
# print(5<=6)

# 5: greater than 
print(a>b)
# print(5>6)

# 6: greater than or eqal to
print(a>=b)
# print(5>=6)

# These condition are use in the if statement and also use in loop