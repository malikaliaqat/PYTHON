# python has set of built_in methods that you can you  on string

# upper string
a = "malika liaqat"
print(a.upper())

# lower string
a = "malika liaqat"
print(a.lower())

#  remove whitespace
a = "malika liaqat"
print(a.strip())


#  replace the string
a = "malika liaqat"
print(a.replace("m ","h"))

# spilt
a = "malika liaqat"
b = a.split(",")
print(b)